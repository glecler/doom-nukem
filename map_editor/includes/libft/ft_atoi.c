int		ft_atoi(char *str)
{
	long long int x;
	long long int y;

	y = 1;
	x = 0;
	if (0 <= *str && *str < 58)
	{
		while (*str == '\t' || *str == '\n' || *str == '\r' || *str == '\v'
				|| *str == '\f' || *str == ' ')
			str++;
		if (*str == '-')
		{
			y = y * -1;
			str++;
		}
		if (*str == '+' && *(str - 1) != '-')
			str++;
		while ('0' <= *str && *str < ':')
		{
			x = x * 10 + ((long long int)*str - 48);
			str++;
		}
	}
	return (x * y);
}
