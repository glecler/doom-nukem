#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t x;
	size_t y;

	x = 0;
	y = ft_strlen(s1);
	while (s2[x])
	{
		s1[x + y] = s2[x];
		x++;
	}
	s1[x + y] = '\0';
	return (s1);
}
